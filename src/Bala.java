public class Bala {
    private double x, y;

    public Bala() {
        this.x = 0;
        this.y = 0;
    }

    public Bala(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
